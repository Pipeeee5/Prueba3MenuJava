    private void jButtonListarCarrerasActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonListarCarrerasActionPerformed
        // TODO add your handling code here:

        jTableVisorCarreras.removeAll();
        DefaultTableModel tabla2x = (DefaultTableModel) jTableVisorCarreras.getModel();//definir la tabla a ocupar

        while (tabla2x.getRowCount() > 0) {   // borrar datos de la tabla "limpiar"
            tabla2x.removeRow(0);
        }
        try {
            for (Carrera forC : CarreraDAO.obtenerDatos()) {// colocar nombre al for
                if (forC instanceof Carrera) {
                    Object[] x = new Object[2];// numero de tablas
                    x[0] = forC.getId();
                    x[1] = forC.getNombreCarrera();


                    tabla2x.addRow(x);//a√±ada informacion que recupera
                }
            }
            jTableVisorCarreras.setModel(tabla2x);// mostrar datos
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "error");
        }
    }//GEN-LAST:event_jButtonListarCarrerasActionPerformed

