    private void jButtonListarContratoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonListarContratoActionPerformed
        // TODO add your handling code here:

        jTableVisorContrato.removeAll();
        DefaultTableModel tabla5x = (DefaultTableModel) jTableVisorContrato.getModel();//definir la tabla a ocupar

        while (tabla5x.getRowCount() > 0) {   // borrar datos de la tabla "limpiar"
            tabla5x.removeRow(0);
        }
        try {
            for (Contrato forC : objContrato.obtenerDatos()) {// colocar nombre al for
                if (forC instanceof Contrato) {
                    Object[] x = new Object[5];// numero de tablas
                    x[0] = forC.getRut();
                    x[1] = forC.getNombre();
                    x[2] = forC.getNotas();
                    x[3] = forC.getNombreApoderado();
                    x[4] = forC.getEdad();

                    tabla5x.addRow(x);//a√±ada informacion que recupera
                }
            }
            jTableVisorContrato.setModel(tabla5x);// mostrar datos
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "error");
        }
    }//GEN-LAST:event_jButtonListarContratoActionPerformed

