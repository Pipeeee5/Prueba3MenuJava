    private void jButtonListarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonListarActionPerformed
        // TODO add your handling code here:

        jTableVisorGeneros.removeAll();
        DefaultTableModel tabla3x= (DefaultTableModel) jTableVisorGeneros.getModel();//definir la tabla a ocupar

        while (tabla3x.getRowCount() > 0) {   // borrar datos de la tabla "limpiar"
            tabla3x.removeRow(0);
        }
        try {
            for (Genero forD : GeneroDAO.obtenerDatos()) {// colocar nombre al for
                if (forD instanceof Genero) {
                    Object[] x = new Object[2];// numero de tablas
                    x[0] = forD.getId();
                    x[1] = forD.getGenero();
                    tabla3x.addRow(x);//a√±ada informacion que recupera
                }
            }
            jTableVisorGeneros.setModel(tabla3x);// mostrar datos
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "error");
        }
    }//GEN-LAST:event_jButtonListarActionPerformed

