    private void jLabelAceptarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jLabelAceptarMouseClicked
        // TODO add your handling code here:
        
        Usuario usuario = null;
        String userName = jTextFieldUsuario.getText();
        String password = jPasswordFieldClave.getText();


        if (!userName.equals("")) {
            usuario = UsuarioDAO.login(userName,password);
            if (usuario != null) {
 
                new Menu().setVisible(true);
                
            } else {
                int x = JOptionPane.ERROR_MESSAGE;
                JOptionPane.showMessageDialog(this, "Error, el usuario o la clave es incorrecto","ERROR 404",x);
                limpiar();
            }
        }
        
    }//GEN-LAST:event_jLabelAceptarMouseClicked

