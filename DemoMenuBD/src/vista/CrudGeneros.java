    private void jButtonAgregarGenerosActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonAgregarGenerosActionPerformed
        // TODO add your handling code here:

        try {
            int id = Integer.valueOf(jTextIdGenero.getText());
            String nombreCarrera = jComboBoxGeneroxd.getSelectedItem().toString();
            if (id >=0 && !nombreCarrera.equals("")) {

                Genero x = new Genero(id, nombreCarrera);
                //String id, String genero, int edad, String genero, String email, String carrera

                if (GeneroDAO.agregar(x) == 1) {
                    JOptionPane.showMessageDialog(rootPane, "Bien, datos grabados");
                } else {
                    JOptionPane.showMessageDialog(rootPane, "Error, id ya existe");
                }
            } else {
                JOptionPane.showMessageDialog(rootPane, "Error, los campos NO deben estar vacios");
            }

            limpiar();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(rootPane, "Error, edad debe ser un n√∫mero");
        }
    }//GEN-LAST:event_jButtonAgregarGenerosActionPerformed

    private void jButtonBuscarGenerosActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonBuscarGenerosActionPerformed
        // TODO add your handling code here:
        Genero genero = null;
        int id = Integer.valueOf(jTextIdGenero.getText());
        jTextIdGenero.setEnabled(false);
        jTextIdGenero.setEnabled(true);

        if (id >=0) {
            genero = GeneroDAO.buscar(String.valueOf(id));
            if (genero != null && genero instanceof Genero) {
                
                jTextIdGenero.setText(String.valueOf(id));
                jComboBoxGeneroxd.setSelectedItem(genero.getGenero());

                jButtonEliminarGeneros.setEnabled(true);
                jButtonModificar.setEnabled(true);

            } else {
                JOptionPane.showMessageDialog(rootPane, "Error, id no debe estar en blanco");
            }
        }
    }//GEN-LAST:event_jButtonBuscarGenerosActionPerformed

